    def format_match_recommendation(self, sport: str, match_data: Dict[str, Any]) -> str:
        """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Ä–µ–∫–æ–º–µ–Ω–¥–∞—Ü–∏—é –ø–æ –º–∞—Ç—á—É —Å–æ–≥–ª–∞—Å–Ω–æ —à–∞–±–ª–æ–Ω—É"""
        template_key = sport
        
        # –î–ª—è –≥–∞–Ω–¥–±–æ–ª–∞ –≤—ã–±–∏—Ä–∞–µ–º —à–∞–±–ª–æ–Ω –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ç–∏–ø–∞ —Å—Ç–∞–≤–∫–∏
        if sport == 'handball':
            if match_data.get('bet_type') == 'total':
                template_key = 'handball_total'
            else:
                template_key = 'handball_victory'
        
        template = OUTPUT_TEMPLATES.get(template_key, "")
        
        try:
            # –ü–æ–¥–≥–æ—Ç–∞–≤–ª–∏–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
            format_data = match_data.copy()
            
            # –î–æ–±–∞–≤–ª—è–µ–º –Ω–µ–¥–æ—Å—Ç–∞—é—â–∏–µ –ø–æ–ª—è –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –≤–∏–¥–æ–≤ —Å–ø–æ—Ä—Ç–∞
            if sport == 'tennis' or sport == 'table_tennis':
                if 'winner' not in format_data:
                    format_data['winner'] = format_data.get('favorite_player', format_data.get('player1', '–ò–≥—Ä–æ–∫'))
            
            if sport == 'football':
                if 'odds' in format_data and isinstance(format_data['odds'], dict):
                    format_data['odds'] = format_data['odds'].get('1', 'N/A')
            
            return template.format(**format_data)
        except KeyError as e:
            logger.error(f"–û—à–∏–±–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è —à–∞–±–ª–æ–Ω–∞ {template_key}: {e}")
